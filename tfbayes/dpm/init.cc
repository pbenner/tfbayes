#ifdef HAVE_CONFIG_H
#include <tfbayes/config.h>
#endif /* HAVE_CONFIG_H */

#include <cstdlib>
#include <ctime>

#include <sys/time.h>

#include <gsl/gsl_rng.h>
#include <gsl/gsl_randist.h>

gsl_rng* _r;

using namespace std;

void __dpm_init__() {
        struct timeval tv;
        gettimeofday(&tv, NULL);
        time_t seed = tv.tv_sec*tv.tv_usec;

        srand(seed);
        _r = gsl_rng_alloc (gsl_rng_default);
        gsl_rng_set(_r, seed);
}
